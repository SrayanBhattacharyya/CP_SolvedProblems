#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    ll t = 1;
    cin >> t;
    while (t--){
        ll n, m;
        cin >> n >> m;
        cout << (n + 1) / 2 << ' ' << (m + 1) / 2 << "\n";
    }    
    return 0;
}